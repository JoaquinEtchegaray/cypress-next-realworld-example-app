Cypress.Commands.add("login", () => {
  cy.visit("/");
  cy.get("[data-cy=sign-in-nav]").click();
  cy.get("[data-cy=email-input]").click().type("commit@studio.com");
  cy.get('[data-cy="password-input"]').type("commit{enter}");
  cy.get("[data-cy=profile-nav]").should("be.visible").contains("commit");
});
